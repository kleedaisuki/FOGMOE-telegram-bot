  next_retry_at TIMESTAMPTZ,
  last_error TEXT,
  created_at TIMESTAMPTZ NOT NULL DEFAULT CURRENT_TIMESTAMP,
  updated_at TIMESTAMPTZ NOT NULL DEFAULT CURRENT_TIMESTAMP,
  completed_at TIMESTAMPTZ,
  source_kind VARCHAR(100) NOT NULL,
  source_key VARCHAR(255) NOT NULL,
  CONSTRAINT conversation_turns_retry_time_ck CHECK (
    (state IN ('inference_retry_wait', 'delivery_retry_wait')) = (next_retry_at IS NOT NULL)
  ),
  CONSTRAINT conversation_turns_completed_at_ck CHECK (
    (state IN ('delivered', 'cancelled', 'failed_final')) = (completed_at IS NOT NULL)
  ),
  CONSTRAINT conversation_turns_source_kind_ck CHECK (
    source_kind ~ '^[a-z][a-z0-9_.-]{0,99}$'
  ),
  CONSTRAINT conversation_turns_source_key_ck CHECK (
    char_length(source_key) BETWEEN 1 AND 255
  ),
  CONSTRAINT conversation_turns_source_identity_uq UNIQUE (
    source_kind,
    source_key
  ),
  CONSTRAINT conversation_turns_telegram_source_ck CHECK (
    (source_kind = 'telegram.update') = (source_update_id IS NOT NULL)
    AND (
      source_kind <> 'telegram.update'
      OR source_key = source_update_id::TEXT
