CREATE FUNCTION observability.ensure_daily_partitions(partition_day DATE)
RETURNS VOID
LANGUAGE plpgsql
SECURITY DEFINER
SET search_path = pg_catalog
AS $$
DECLARE
  lower_bound TIMESTAMPTZ := partition_day::TIMESTAMP AT TIME ZONE 'UTC';
  upper_bound TIMESTAMPTZ := (partition_day + 1)::TIMESTAMP AT TIME ZONE 'UTC';
  suffix TEXT := to_char(partition_day, 'YYYYMMDD');
BEGIN
  IF partition_day < DATE '2020-01-01'
     OR partition_day > (CURRENT_DATE + 366) THEN
    RAISE EXCEPTION 'observability partition day out of allowed range: %', partition_day;
  END IF;

  EXECUTE format(
    'CREATE TABLE IF NOT EXISTS observability.%I PARTITION OF observability.log_records FOR VALUES FROM (%L) TO (%L)',
    'log_records_' || suffix, lower_bound, upper_bound
  );
  EXECUTE format(
    'CREATE TABLE IF NOT EXISTS observability.%I PARTITION OF observability.spans FOR VALUES FROM (%L) TO (%L)',
    'spans_' || suffix, lower_bound, upper_bound
  );
  EXECUTE format(
    'CREATE TABLE IF NOT EXISTS observability.%I PARTITION OF observability.metric_points FOR VALUES FROM (%L) TO (%L)',
    'metric_points_' || suffix, lower_bound, upper_bound
  );
END;
$$;

CREATE FUNCTION observability.drop_partitions_before(retain_from DATE)
RETURNS INTEGER
LANGUAGE plpgsql
SECURITY DEFINER
SET search_path = pg_catalog
AS $$
DECLARE
  child RECORD;
  dropped INTEGER := 0;
  partition_day DATE;
BEGIN
  IF retain_from < DATE '2020-01-01' OR retain_from > CURRENT_DATE THEN
    RAISE EXCEPTION 'observability retention boundary out of allowed range: %', retain_from;
  END IF;

  FOR child IN
    SELECT child_namespace.nspname AS schema_name,
           child_class.relname AS table_name
    FROM pg_inherits
    JOIN pg_class AS parent_class ON parent_class.oid = inhparent
    JOIN pg_namespace AS parent_namespace
      ON parent_namespace.oid = parent_class.relnamespace
    JOIN pg_class AS child_class ON child_class.oid = inhrelid
    JOIN pg_namespace AS child_namespace
      ON child_namespace.oid = child_class.relnamespace
    WHERE parent_namespace.nspname = 'observability'
      AND parent_class.relname IN ('log_records', 'spans', 'metric_points')
  LOOP
    IF child.table_name ~ '^[a-z_]+_[0-9]{8}$' THEN
      partition_day := to_date(right(child.table_name, 8), 'YYYYMMDD');
      IF partition_day < retain_from THEN
        EXECUTE format('DROP TABLE %I.%I', child.schema_name, child.table_name);
        dropped := dropped + 1;
      END IF;
    END IF;
  END LOOP;
  RETURN dropped;
END;
$$;

CREATE VIEW observability.pipeline_health AS
SELECT 'inbox'::TEXT AS stage,
       count(*) FILTER (WHERE status = 'pending') AS pending_count,
       count(*) FILTER (WHERE status = 'processing') AS processing_count,
       count(*) FILTER (WHERE status = 'retry_wait') AS retry_count,
       count(*) FILTER (WHERE status = 'failed_final') AS failed_final_count,
       min(next_attempt_at) FILTER (
         WHERE status IN ('pending', 'retry_wait')
       ) AS oldest_ready_at,
       count(*) FILTER (
         WHERE status = 'processing' AND lease_expires_at < CURRENT_TIMESTAMP
       ) AS expired_lease_count
FROM conversation.inbound_updates
UNION ALL
SELECT 'inference',
       count(*) FILTER (WHERE status = 'pending'),
       count(*) FILTER (WHERE status = 'processing'),
       count(*) FILTER (WHERE status = 'retry'),
       count(*) FILTER (WHERE status = 'failed'),
       min(next_attempt_at) FILTER (WHERE status IN ('pending', 'retry')),
       count(*) FILTER (
         WHERE status = 'processing' AND lease_expires_at < CURRENT_TIMESTAMP
       )
FROM conversation.inference_activities
UNION ALL
SELECT 'outbox',
       count(*) FILTER (WHERE status = 'pending'),
       count(*) FILTER (WHERE status = 'processing'),
       count(*) FILTER (WHERE status = 'retry_wait'),
       count(*) FILTER (WHERE status = 'failed_final'),
       min(next_attempt_at) FILTER (
         WHERE status IN ('pending', 'retry_wait')
       ),
       count(*) FILTER (
         WHERE status = 'processing' AND lease_expires_at < CURRENT_TIMESTAMP
       )
FROM conversation.outbound_messages;

CREATE VIEW observability.turn_latency AS
SELECT turn.turn_id,
       turn.source_update_id AS update_id,
       turn.state,
       inbound.received_at,
       turn.created_at AS turn_created_at,
       activity.created_at AS inference_created_at,
       activity.completed_at AS inference_completed_at,
       delivery.first_created_at AS delivery_created_at,
       delivery.last_delivered_at AS delivered_at,
       EXTRACT(EPOCH FROM (turn.created_at - inbound.received_at)) * 1000
         AS ingress_to_turn_ms,
       EXTRACT(EPOCH FROM (activity.completed_at - activity.created_at)) * 1000
         AS inference_total_ms,
       EXTRACT(EPOCH FROM (delivery.last_delivered_at - delivery.first_created_at)) * 1000
         AS delivery_total_ms,
       EXTRACT(EPOCH FROM (turn.completed_at - inbound.received_at)) * 1000
         AS end_to_end_ms,
       turn.inference_attempts,
       turn.delivery_attempts
FROM conversation.conversation_turns AS turn
LEFT JOIN conversation.inbound_updates AS inbound
  ON inbound.update_id = turn.source_update_id
LEFT JOIN conversation.inference_activities AS activity
  ON activity.turn_id = turn.turn_id
LEFT JOIN LATERAL (
  SELECT min(outbound.created_at) AS first_created_at,
         max(outbound.delivered_at) AS last_delivered_at
  FROM conversation.outbound_messages AS outbound
  WHERE outbound.turn_id = turn.turn_id
) AS delivery ON TRUE;

