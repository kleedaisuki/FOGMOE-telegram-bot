@dataclass(frozen=True, slots=True)
class PendingTokenRequestsResult:
    """@brief 待审批代币申请列表结果 / Pending token-request list result.

    @param code 稳定银行结果码 / Stable bank result code.
    @param requests 按申请时间排序的待审批申请 / Pending requests ordered by request time.
    """

    code: BankCode
    """@brief 稳定银行结果码 / Stable bank result code."""

    requests: tuple[TokenRequest, ...] = ()
    """@brief 待审批申请快照 / Pending request snapshots."""

    def __post_init__(self) -> None:
        """@brief 验证失败结果不会泄漏申请 / Validate failures do not expose requests.

        @return None / None.
        @raise TypeError 或 ValueError 结果形状不合法时抛出 /
            Raised when the result shape is invalid.
        """

        if not isinstance(self.code, BankCode):
            raise TypeError("Pending-token-requests result code must be BankCode")
        if self.code is not BankCode.SUCCESS and self.requests:
            raise ValueError("Failed pending-request result cannot carry requests")
        if any(
            request.status is not TokenRequestStatus.PENDING
            for request in self.requests
        ):
            raise ValueError("Pending-request result can contain only pending requests")


@dataclass(frozen=True, slots=True)
class ActivityPotFundingResult:
    """@brief 活动奖池注资结果 / Result of an activity-pot funding operation.

    @param code 稳定银行结果码 / Stable bank result code.
    @param amount 成功时的本次注资数量 / Funding amount when successful.
    @param activity_pot_balance 成功后的活动奖池余额 / Activity-pot balance after success.
    @param ledger_entry_id 成功时不可变账本分录标识 / Immutable ledger-entry ID on success.
    @param replayed 是否由同义幂等重放返回 / Whether returned from a semantic idempotent replay.
    """

    code: BankCode
    """@brief 稳定银行结果码 / Stable bank result code."""

    amount: TokenAmount | None = None
    """@brief 可选本次注资量 / Optional funding amount."""

    activity_pot_balance: int | None = None
    """@brief 可选注资后奖池余额 / Optional post-funding pot balance."""

    ledger_entry_id: UUID | None = None
    """@brief 可选账本分录标识 / Optional ledger-entry identifier."""

    replayed: bool = False
    """@brief 幂等回放标记 / Idempotent replay marker."""

    def __post_init__(self) -> None:
        """@brief 验证成功结果包含完整可审计事实 / Validate successful result completeness.

        @return None / None.
        @raise TypeError 或 ValueError 成功/失败结果形状不一致时抛出 /
            Raised when the success/failure result shape is inconsistent.
        """

        if not isinstance(self.code, BankCode):
            raise TypeError("Activity-pot funding result code must be BankCode")
        if self.code is BankCode.SUCCESS:
            if (
                self.amount is None
                or self.activity_pot_balance is None
                or self.ledger_entry_id is None
            ):
                raise ValueError(
                    "Successful activity-pot funding requires amount, balance, and ledger entry"
                )
            if self.activity_pot_balance < self.amount.value:
                raise ValueError(
                    "Activity-pot balance cannot be below the just-funded amount"
                )
        elif (
            self.amount is not None
            or self.activity_pot_balance is not None
            or self.ledger_entry_id is not None
        ):
            raise ValueError("Failed activity-pot funding cannot carry funding facts")
