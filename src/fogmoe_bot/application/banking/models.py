@dataclass(frozen=True, slots=True)
class IssueTokens:
    """@brief 银行管理员直接发行代币 / Direct token issuance by a bank administrator.

    @param administrator_id 银行管理员 / Bank administrator.
    @param recipient_id 收款用户 / Recipient user.
    @param amount 正数发行量 / Positive issuance amount.
    @param bucket 收款钱包类别 / Recipient wallet bucket.
    @param purpose 可审计用途 / Auditable purpose.
    @param issued_at 发行时刻 / Issuance instant.
    @param idempotency_key 幂等键 / Idempotency key.
    """

    administrator_id: int
    """@brief 银行管理员 / Bank administrator."""

    recipient_id: int
    """@brief 收款用户 / Recipient user."""

    amount: TokenAmount
    """@brief 正数发行量 / Positive issuance amount."""

    bucket: TokenBucket
    """@brief 收款钱包类别 / Recipient wallet bucket."""

    purpose: str
    """@brief 可审计用途 / Auditable purpose."""

    issued_at: datetime
    """@brief 发行时刻 / Issuance instant."""

    idempotency_key: str
    """@brief 业务幂等键 / Business idempotency key."""

    def __post_init__(self) -> None:
        """@brief 验证发行命令 / Validate the issuance command.

        @return None / None.
        @raise ValueError 标识、用途、时间或幂等键非法时抛出 /
            Raised when identities, purpose, time, or idempotency key are invalid.
        """

        if self.administrator_id <= 0 or self.recipient_id <= 0:
            raise ValueError("Issuance identities must be positive")
        if self.bucket is not TokenBucket.FREE:
            raise ValueError(
                "Regular bank issuance is limited to the free token bucket"
            )
        if not 1 <= len(self.purpose.strip()) <= 500:
            raise ValueError("Issuance purpose must contain 1-500 characters")
        if self.issued_at.tzinfo is None or self.issued_at.utcoffset() is None:
            raise ValueError("Issuance time must be timezone-aware")
        if not self.idempotency_key.strip() or len(self.idempotency_key) > 200:
            raise ValueError("Issuance idempotency key must contain 1-200 characters")


