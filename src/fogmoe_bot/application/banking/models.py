@dataclass(frozen=True, slots=True)
class BankOverview:
    """@brief 用户银行钱包概览 / User bank-wallet overview.

    @param user_id 用户标识 / User identity.
    @param free 免费钱包余额 / Free-wallet balance.
    @param paid 付费钱包余额 / Paid-wallet balance.
    """

    user_id: int
    """@brief 用户标识 / User identity."""

    free: WalletBalance
    """@brief 免费钱包余额 / Free-wallet balance."""

    paid: WalletBalance
    """@brief 付费钱包余额 / Paid-wallet balance."""

    def __post_init__(self) -> None:
        """@brief 验证双钱包概览 / Validate dual-wallet overview.

        @return None / None.
        @raise ValueError 用户或钱包类别不匹配时抛出 /
            Raised when user or wallet buckets do not match.
        """

        if self.user_id <= 0:
            raise ValueError("Bank overview user must be positive")
        if self.free.bucket is not TokenBucket.FREE:
            raise ValueError("Bank overview free balance must use free bucket")
        if self.paid.bucket is not TokenBucket.PAID:
            raise ValueError("Bank overview paid balance must use paid bucket")

    @property
    def total(self) -> int:
        """@brief 返回两个钱包的总余额 / Return total across both wallets.

        @return 非负总余额 / Non-negative total balance.
        """

        return self.free.value + self.paid.value


