        """@brief 验证审核命令 / Validate the review command.

        @return None / None.
        @raise ValueError 标识、时间或幂等键非法时抛出 /
            Raised when identifiers, time, or idempotency key are invalid.
        """

        if self.reviewer_id <= 0:
            raise ValueError("Token reviewer must be positive")
        if self.reviewed_at.tzinfo is None or self.reviewed_at.utcoffset() is None:
            raise ValueError("Token review time must be timezone-aware")
        if not self.idempotency_key.strip() or len(self.idempotency_key) > 200:
            raise ValueError(
                "Token review idempotency key must contain 1-200 characters"
            )
