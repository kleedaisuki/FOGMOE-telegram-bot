"""@brief 银行应用命令和结果 / Banking application commands and results."""

from __future__ import annotations

from dataclasses import dataclass
from datetime import datetime
from enum import StrEnum
from uuid import UUID

from fogmoe_bot.domain.banking.money import TokenAmount, TokenBucket, WalletBalance
from fogmoe_bot.domain.banking.requests import TokenRequest, TokenRequestStatus


class BankCode(StrEnum):
    """@brief 银行用例结果代码 / Bank use-case result code."""

    SUCCESS = "success"
    """@brief 操作成功 / Operation succeeded."""

    NOT_REGISTERED = "not_registered"
    """@brief 用户账户未注册 / User account is not registered."""

    NOT_FOUND = "not_found"
    """@brief 请求不存在 / Request does not exist."""

    NOT_PENDING = "not_pending"
    """@brief 请求不再等待审核 / Request is no longer awaiting review."""

    FORBIDDEN = "forbidden"
    """@brief 操作者无权限 / Actor is not authorized."""

    INSUFFICIENT_FUNDS = "insufficient_funds"
    """@brief 可用资金不足 / Available funds are insufficient."""


class TokenReviewDecision(StrEnum):
    """@brief 管理员对请求的审核决定 / Administrator review decision."""

    APPROVE = "approve"
    """@brief 批准并发行免费代币 / Approve and issue free tokens."""

    REJECT = "reject"
    """@brief 拒绝且不发行代币 / Reject without issuing tokens."""


@dataclass(frozen=True, slots=True)
class RequestTokens:
    """@brief 用户请求免费活动代币 / User request for free activity tokens.

    @param user_id 申请用户 / Requesting user.
    @param amount 请求金额 / Requested amount.
    @param purpose 用途说明 / Purpose statement.
    @param requested_at 请求时刻 / Request instant.
    @param idempotency_key 幂等键 / Idempotency key.
    @param request_id 由入口预先生成的请求 ID / Request ID generated by the entry point.
    """

    user_id: int
    """@brief 申请用户 / Requesting user."""

    amount: TokenAmount
    """@brief 请求金额 / Requested amount."""

    purpose: str
    """@brief 用途说明 / Purpose statement."""

    requested_at: datetime
    """@brief 请求时刻 / Request instant."""

    idempotency_key: str
    """@brief 业务幂等键 / Business idempotency key."""

    request_id: UUID
    """@brief 请求稳定标识 / Stable request identity."""

    def __post_init__(self) -> None:
        """@brief 验证请求命令 / Validate the request command.

        @return None / None.
        @raise ValueError 标识、时间或幂等键非法时抛出 /
            Raised when identifiers, time, or idempotency key are invalid.
        """

        if self.user_id <= 0:
            raise ValueError("Token requester must be positive")
        if not 1 <= len(self.purpose.strip()) <= 500:
            raise ValueError("Token request purpose must contain 1-500 characters")
        if self.requested_at.tzinfo is None or self.requested_at.utcoffset() is None:
            raise ValueError("Token request time must be timezone-aware")
        if not self.idempotency_key.strip() or len(self.idempotency_key) > 200:
            raise ValueError(
                "Token request idempotency key must contain 1-200 characters"
            )

    def aggregate(self) -> TokenRequest:
        """@brief 转换为待处理领域聚合 / Convert to a pending domain aggregate.

        @return 待审核代币请求 / Pending token request.
        """

        return TokenRequest(
            request_id=self.request_id,
            requester_id=self.user_id,
            requested_amount=self.amount,
            requested_bucket=TokenBucket.FREE,
            purpose=self.purpose,
            status=TokenRequestStatus.PENDING,
            requested_at=self.requested_at,
        )


