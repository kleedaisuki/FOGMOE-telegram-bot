@dataclass(frozen=True, slots=True)
class TownResult:
    """@brief 群组小镇操作的稳定结果 / Stable result of a group-town operation.

    @param code 操作结果代码 / Operation result code.
    @param town 操作后的可选小镇快照 / Optional town snapshot after the operation.
    @param project 操作涉及的可选项目 / Optional project involved in the operation.
    @param contribution 操作产生的可选贡献 / Optional contribution produced by the operation.
    @param replayed 是否来自幂等回放 / Whether result came from idempotent replay.
    """

    code: TownCode
    """@brief 操作结果代码 / Operation result code."""

    town: Town | None = None
    """@brief 可选小镇快照 / Optional town snapshot."""

    project: TownProject | None = None
    """@brief 可选项目快照 / Optional project snapshot."""

    contribution: TownContribution | None = None
    """@brief 可选贡献快照 / Optional contribution snapshot."""

    replayed: bool = False
    """@brief 是否来自幂等回放 / Whether this is an idempotent replay."""

    def __post_init__(self) -> None:
        """@brief 验证小镇结果载荷类型 / Validate town-result payload types.

        @return None / None.
        @raise TypeError 结果代码或可选载荷类型非法时抛出 /
            Raised when result code or optional payload types are invalid.
        """

        if not isinstance(self.code, TownCode):
            raise TypeError("Town result code must be a TownCode")
        if self.town is not None and not isinstance(self.town, Town):
            raise TypeError("Town result town must be a Town")
        if self.project is not None and not isinstance(self.project, TownProject):
            raise TypeError("Town result project must be a TownProject")
        if self.contribution is not None and not isinstance(
            self.contribution, TownContribution
        ):
            raise TypeError("Town result contribution must be a TownContribution")
        if not isinstance(self.replayed, bool):
            raise TypeError("Town result replayed flag must be a bool")
