@dataclass(frozen=True, slots=True)
class BillingResult:
    """@brief Billing 写用例的可重放结果 / Replayable result of a Billing write use case.

    @param code 结果代码 / Result code.
    @param order 可选订单快照 / Optional order snapshot.
    @param refund 可选退款快照 / Optional refund snapshot.
    @param entitlements 本次履约或撤销涉及的权益 / Entitlements involved in fulfillment or revocation.
    @param subscription 可选订阅快照 / Optional subscription snapshot.
    @param replayed 结果是否来自幂等重放 / Whether result came from idempotent replay.
    """

    code: BillingCode
