class BillingCode(StrEnum):
    """@brief Billing 用例结果代码 / Billing use-case result code."""

    SUCCESS = "success"
    """@brief 操作已成功提交 / Operation committed successfully."""

    NOT_REGISTERED = "not_registered"
    """@brief 用户尚无账户 / User does not have an account."""

    NOT_FOUND = "not_found"
    """@brief 产品、订单、退款或订阅不存在 / Product, order, refund, or subscription was not found."""

    FORBIDDEN = "forbidden"
    """@brief 操作者无权限 / Actor is not authorized."""

    OFFER_UNAVAILABLE = "offer_unavailable"
    """@brief 报价不再可售 / Offer is no longer available."""

    PAYMENT_UNVERIFIED = "payment_unverified"
    """@brief 渠道事件未通过外部验证 / Provider event did not pass external verification."""

    INVALID_PAYMENT_EVENT = "invalid_payment_event"
    """@brief 事件类型不适用于该用例 / Event kind does not apply to this use case."""

    INVALID_STATE = "invalid_state"
    """@brief 聚合不处于可变迁状态 / Aggregate is not in a transitionable state."""

    CONFLICT = "conflict"
    """@brief 幂等键或乐观并发发生冲突 / Idempotency or optimistic-concurrency conflict."""


class RefundReviewDecision(StrEnum):
    """@brief 后台对退款申请的审核决定 / Back-office review decision for a refund request."""

    APPROVE = "approve"
    """@brief 批准并等待支付渠道结算 / Approve and await provider settlement."""

    REJECT = "reject"
    """@brief 拒绝且恢复原订单履约状态 / Reject and restore original order fulfillment state."""


@dataclass(frozen=True, slots=True)
class PlaceOrder:
    """@brief 以当前可售报价创建订单 / Create an order from a currently sellable offer.

    @param buyer_id 下单用户 / Ordering user.
    @param offer_id 报价标识 / Offer identity.
    @param order_id 入口预先生成的订单标识 / Order identity generated by the entry point.
    @param created_at 创建时刻 / Creation instant.
    @param idempotency_key 业务幂等键 / Business idempotency key.
    @param renewal_subscription_id 可选待续费订阅标识 / Optional subscription identity to renew.
    @note 客户端不能传入价格；端口必须冻结已验证 Offer 的原生价格。/
        A client cannot provide a price; the port must freeze the verified Offer's native price.
    """

    buyer_id: int
    """@brief 下单用户 / Ordering user."""

    offer_id: UUID
    """@brief 报价标识 / Offer identity."""

    order_id: UUID
    """@brief 订单标识 / Order identity."""

    created_at: datetime
    """@brief 创建时刻 / Creation instant."""

    idempotency_key: str
    """@brief 业务幂等键 / Business idempotency key."""

    renewal_subscription_id: UUID | None = None
    """@brief 可选待续费订阅标识 / Optional subscription identity to renew."""

    def __post_init__(self) -> None:
        """@brief 验证下单命令 / Validate order-placement command.

        @return None / None.
        @raise ValueError 用户、时刻或幂等键非法时抛出 /
            Raised when user, instant, or idempotency key is invalid.
        """

        _require_positive_user(self.buyer_id, field="Order buyer")
        object.__setattr__(
            self,
            "created_at",
            normalize_instant(self.created_at, field="Order creation time"),
        )
        object.__setattr__(
            self,
            "idempotency_key",
            _normalize_idempotency_key(self.idempotency_key),
        )


@dataclass(frozen=True, slots=True)
class RecordPaymentEvent:
    """@brief 记录已验证的付款或争议事件 / Record a verified payment or chargeback event.

    @param event 已验真的支付渠道事件 / Verified payment-provider event.
    @param idempotency_key 业务幂等键 / Business idempotency key.
    @note Refund 事件必须使用 ``SettleRefund``，从而与 Refund 聚合一起结算。/
        Refund events must use ``SettleRefund`` so they settle with the Refund aggregate.
    """

    event: PaymentEvent
    """@brief 已验真的支付渠道事件 / Verified payment-provider event."""

    idempotency_key: str
    """@brief 业务幂等键 / Business idempotency key."""

    def __post_init__(self) -> None:
        """@brief 验证普通支付事件命令 / Validate ordinary payment-event command.

        @return None / None.
        @raise TypeError 事件类型非法时抛出 / Raised when event has an invalid type.
        @raise ValueError 事件是退款事件或幂等键非法时抛出 /
            Raised when event is a refund event or key is invalid.
        """

        if not isinstance(self.event, PaymentEvent):
            raise TypeError("Billing payment event must be a PaymentEvent")
        if self.event.kind in {
            PaymentEventKind.REFUND_SUCCEEDED,
            PaymentEventKind.REFUND_FAILED,
        }:
            raise ValueError("Refund events must be recorded through SettleRefund")
        object.__setattr__(
            self,
            "idempotency_key",
            _normalize_idempotency_key(self.idempotency_key),
        )


